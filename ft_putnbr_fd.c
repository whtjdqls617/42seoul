#include "libft.h"

static void		write_nbr(int n, int fd)
{
	if (n >= 10)
		write_nbr(n / 10, fd);
	write(fd, &"0123456789"[n % 10], 1);
}

void			ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		n *= -1;
		write(fd, "-", 1);
	}
	write_nbr(n, fd);
}
